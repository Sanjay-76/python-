# Do not allows a Duplicate,Duplicates values will  be removed
# Any type of data can be stored
# We can't modify the set items but we can add or remove items
# Sets are unordered
# add(),update(),remove(),pop()
a={1,2,4,5,}
a.add(7)
print(a)

b={1,2,4,5,}
b.update()
print(b)

c={1,2,4,5,}
c.pop()
print(c)

d={1,2,4,5,}
d.remove()
print(d)